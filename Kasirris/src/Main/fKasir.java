    private void txtBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBayarActionPerformed
        try {
            int hargaTotal = Integer.parseInt(txtHargaTotal.getText().replace(".", ""));
            int bayar = Integer.parseInt(txtBayar.getText());

            if (bayar >= hargaTotal) {
                txtKembalian.setText(dec.format(bayar - hargaTotal));
            } else {
                JOptionPane.showMessageDialog(this, "Uang kurang untuk pembayaran ini!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mohon input data secara benar!", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Mohon input bayar!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtBayarActionPerformed

