    private void btnTambahkanItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahkanItemActionPerformed
        if (!txtKodeBarang.getText().isBlank() && !txtNamaBarang.getText().isBlank() && !txtKuantitas.getText().isBlank() && Integer.parseInt(txtKuantitas.getText()) > 0) {
            LocalDateTime date = LocalDateTime.now();
            DateTimeFormatter dateFormat = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
            String[] row = {txtNamaBarang.getText().toUpperCase(), txtKuantitas.getText()};
            maindtm.addRow(row);
            String[] history = {dateFormat.format(date), currentUser, txtNamaBarang.getText().toUpperCase(), txtKuantitas.getText()};
            historydtm.addRow(history);
            System.out.println(time() + " | " + currentUser + " menambahkan " + txtNamaBarang.getText().toUpperCase() + "(" + txtKodeBarang.getText() + ")");
            int hargaTotal = Integer.parseInt(txtHargaTotal.getText().replace(".", ""));
            int harga = Integer.parseInt(txtHarga.getText().replace(".", ""));
            int kuantitas = Integer.parseInt(txtKuantitas.getText());
            hargaTotal += (harga * kuantitas);
            txtHargaTotal.setText(dec.format(hargaTotal));
        } else {
            JOptionPane.showMessageDialog(this, "Ada kesalahan pada data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahkanItemActionPerformed

