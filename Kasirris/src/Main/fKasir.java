    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int index = MaintblMain.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu item di Tabel!", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            int hargaTotal = Integer.parseInt(txtHargaTotal.getText().replace(".", ""));
            int harga = price.get(item.indexOf(maindtm.getValueAt(index, 0)));
            int kuantitas = Integer.parseInt(String.valueOf(maindtm.getValueAt(index, 1)));
            hargaTotal -= (harga * kuantitas);
            maindtm.removeRow(index);
            if (firstTime) {
                historydtm.removeRow(index);
            } else {
                historydtm.removeRow(((historydtm.getRowCount() - 1) - maindtm.getRowCount()) + index);
            }
            txtHargaTotal.setText(dec.format(hargaTotal));
            System.out.println(time() + " | " + currentUser + " mengurangi " + txtNamaBarang.getText().toUpperCase() + "(" + txtKodeBarang.getText() + ")");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

